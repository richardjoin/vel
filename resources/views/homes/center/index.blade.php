function friend_state(uid,callback){
	$.ajax({
		url: 'http://bbs.hyphp.cn/friend/friend_state.html',
		type:"POST",
		cache: false,
		data:{
			uid:uid,
		},
		dataType: 'json'
	}).then(function(e) {
		callback(e.error,e.id);
	}, function() {
		swal("失败", "请尝试重新提交", "error");
	});
}
